using StatePattern.Examples.Metanit.Interfaces;

namespace StatePattern.Examples.Metanit.Entity
{
    class Water
    {
        public IWaterState State { get; set; }

        public Water(IWaterState ws)
        {
            State = ws;
        }

        public void Heat()
        {
            State.Heat(this);
        }
        public void Frost()
        {
            State.Frost(this);
        }
    }
}
